# Función para calcular área de rectángulo
def calcular_area(largo, ancho):
    return largo * ancho

# Cálculos de áreas
area1 = calcular_area(5, 3)
area2 = calcular_area(7, 4)

print(f"Área 1: {area1}")
print(f"Área 2: {area2}")

# Función simple para sumar
def sumar_numeros(a, b):
    resultado = a + b
    return resultado

# Operaciones de suma
suma1 = sumar_numeros(10, 20)
suma2 = sumar_numeros(15, 25)

print(f"Suma 1: {suma1}")
print(f"Suma 2: {suma2}")